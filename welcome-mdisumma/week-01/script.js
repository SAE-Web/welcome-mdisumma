// Types of value
var typeOfNumber = 4;
var typeOfString = "I am a string";
var typeOfBoolean = false;
var typeOfNull = null;
var typeOfUndefined;

// numeric data (stores regular number and integers)
console.log ("var typeOfNumber = 4; type is");
console.log (typeof typeOfNumber);

// string data (stores string of letters and symbols)
console.log ("var typeOfString = \"I am a string\";");
console.log(typeof typeOfString);

// boolean data (stores one or the other of the binary pair true/false)
console.log ("var typeOfBoolean = false;");
console.log(typeof typeOfBoolean);

// null data (stores the intentional absence of a value)
console.log ("var typeOfNull = null;");
console.log(typeof typeOfNull);

//undefined data (placeholder when a variable is not set)
console.log ("var typeOfUndefined;");
console.log(typeof typeOfUndefined);

